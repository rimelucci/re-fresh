$("#top").mouseenter(function(){
        $("#top").animate({opacity: '0.8'})
});

$("#top").mouseleave(function(){
        $("#top").animate({opacity: '1'})
});

$("#bottom").mouseenter(function(){
        $("#bottom").animate({opacity: '0.8'})
});

$("#bottom").mouseleave(function(){
        $("#bottom").animate({opacity: '1'})
});
